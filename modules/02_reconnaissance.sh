#!/bin/bash
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; WHITE='\033[1;37m'; DIM='\033[2m'; RESET='\033[0m'

CYBERTUZ_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
if [ -z "$CYBERTUZ_LANG" ]; then
    source "$CYBERTUZ_DIR/lang.sh"
    ct_load_lang || _ct_set_lang "en"
fi

pause_modul() { echo ""; echo -ne "${DIM}  $CT_ENTER${RESET}"; read -r; }

menu_jaringan() {
    while true; do
        clear
        echo -e "${RED}=== $CT_MOD_HDR_2 ===${RESET}"
        echo ""
        echo -e "  ${GREEN}[1]${RESET}  $CT_M2_1"
        echo -e "  ${GREEN}[2]${RESET}  $CT_M2_2"
        echo -e "  ${GREEN}[3]${RESET}  $CT_M2_3"
        echo -e "  ${GREEN}[4]${RESET}  $CT_M2_4"
        echo -e "  ${GREEN}[5]${RESET}  $CT_M2_5"
        echo -e "  ${GREEN}[6]${RESET}  $CT_M2_6"
        echo -e "  ${GREEN}[7]${RESET}  $CT_M2_7"
        echo -e "  ${GREEN}[8]${RESET}  $CT_M2_8"
        echo -e "  ${GREEN}[9]${RESET}  $CT_M2_9"
        echo -e "  ${RED}[0]${RESET}  $CT_BACK"
        echo ""
        echo -ne "  ${CYAN}$CT_MPROMPT${RESET}"
        read -r choice
        case $choice in
            1) belajar_osi ;;
            2) belajar_ip ;;
            3) belajar_protokol ;;
            4) belajar_dns ;;
            5) belajar_http ;;
            6) belajar_firewall ;;
            7) simulasi_packet ;;
            8) simulasi_jaringan ;;
            9) lab_koneksi_lokal ;;
            0) return ;;
            *) echo -e "${RED}$CT_INVALID${RESET}"; sleep 1 ;;
        esac
    done
}

