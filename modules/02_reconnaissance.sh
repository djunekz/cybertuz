    echo "  Class D: 224.0.0.0 - 239.255.255.255 (Multicast)"
    echo "  Class E: 240.0.0.0 - 255.255.255.255 (Reserved)"
    echo ""
    echo -e "${WHITE}$CT_C_M2_PRIVATE${RESET}"
    echo "  10.0.0.0/8      (Class A private)"
    echo "  172.16.0.0/12   (Class B private)"
    echo "  192.168.0.0/16  (Class C private)"
    echo "  127.0.0.0/8     (Loopback)"
    echo ""
    echo -e "${WHITE}$CT_C_M2_SUBNET${RESET}"
    echo "  /8  = 255.0.0.0       = 16,777,214 hosts"
    echo "  /16 = 255.255.0.0     = 65,534 hosts"
    echo "  /24 = 255.255.255.0   = 254 hosts"
    echo "  /25 = 255.255.255.128 = 126 hosts"
    echo "  /26 = 255.255.255.192 = 62 hosts"
    echo "  /27 = 255.255.255.224 = 30 hosts"
    echo "  /28 = 255.255.255.240 = 14 hosts"
    echo "  /29 = 255.255.255.248 = 6 hosts"
    echo "  /30 = 255.255.255.252 = 2 hosts"
    echo ""
    echo -e "${WHITE}$CT_C_M2_SUBNET_CALC${RESET}"
    echo "  192.168.1.0/24"
    echo "  $CT_C_M2_NET_ADDR : 192.168.1.0"
    echo "  $CT_C_M2_BROADCAST       : 192.168.1.255"
    echo "  $CT_C_M2_USABLE    : 192.168.1.1 - 192.168.1.254"
    echo "  $CT_C_M2_TOTAL_HOST      : 254"
    echo ""
    echo -e "${WHITE}$CT_C_M2_IPV6${RESET}"
    echo "  $CT_C_M2_IPV6_DESC"
    echo "  2001:0db8:85a3:0000:0000:8a2e:0370:7334"
    echo "  -> 2001:db8:85a3::8a2e:370:7334"
    echo ""
    echo -e "${WHITE}$CT_C_M2_SUBNET_SIM${RESET}"
    echo -ne "  $CT_C_M2_ENTER_IP"
    read -r ip_cidr
    if [[ "$ip_cidr" =~ ^([0-9]+\.[0-9]+\.[0-9]+\.[0-9]+)/([0-9]+)$ ]]; then
        local ip="${BASH_REMATCH[1]}" cidr="${BASH_REMATCH[2]}"
        local hosts=$((2 ** (32 - cidr) - 2))
        echo ""
        echo "  IP: $ip | CIDR: /$cidr | $CT_C_M2_NUM_HOSTS: $hosts"
    else
        echo "  $CT_INVALID - Format: X.X.X.X/XX"
    fi
    pause_modul
}

