lab_koneksi_lokal() {
    clear
    echo -e "${YELLOW}=== $CT_C_M2_LAB_TITLE ===${RESET}"
    echo ""
    echo "$CT_C_M2_LAB_INTRO"
    echo ""
    echo -e "${WHITE}$CT_C_M2_CHECK_IP${RESET}"
    if command -v ip &>/dev/null; then
        ip addr show 2>/dev/null | grep -E "(inet |inet6)" | head -5
    elif command -v ifconfig &>/dev/null; then
        ifconfig 2>/dev/null | grep "inet " | head -5
    else
        echo "  $CT_C_M2_NOT_AVAIL"
    fi
    echo ""
    echo -e "${WHITE}$CT_C_M2_CHECK_GW${RESET}"
    ip route 2>/dev/null | head -5 || route -n 2>/dev/null | head -5 || echo "  $CT_C_M2_NOT_AVAIL"
    echo ""
    echo -e "${WHITE}$CT_C_M2_CHECK_CONN${RESET}"
    if command -v ss &>/dev/null; then
        ss -tuln 2>/dev/null | head -10
    elif command -v netstat &>/dev/null; then
        netstat -tuln 2>/dev/null | head -10
    else
        echo "  $CT_C_M2_NOT_AVAIL"
    fi
    echo ""
    echo -e "${WHITE}$CT_C_M2_CHECK_ARP${RESET}"
    arp -a 2>/dev/null | head -5 || ip neigh 2>/dev/null | head -5 || echo "  $CT_C_M2_NOT_AVAIL"
    echo ""
    echo -e "${WHITE}$CT_C_M2_CHECK_DNS${RESET}"
    cat /etc/resolv.conf 2>/dev/null || echo "  $CT_C_M2_NOT_AVAIL"
    pause_modul
}

