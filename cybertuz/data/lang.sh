ct_load_lang() {
    if [ -f "$LANG_SAVE_FILE" ]; then
        local saved
        saved=$(cat "$LANG_SAVE_FILE" 2>/dev/null | tr -d '[:space:]')
        if [ -n "$saved" ]; then
            _ct_set_lang "$saved"
            return 0
        fi
    fi
    return 1
}

ct_save_lang() {
    echo "$1" > "$LANG_SAVE_FILE"
}

