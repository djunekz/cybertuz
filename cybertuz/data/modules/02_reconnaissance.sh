pause_modul() {
    echo ""
    echo -ne "${DIM}  $CT_ENTER${RESET}"
    read -r
}

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
WHITE='\033[1;37m'
