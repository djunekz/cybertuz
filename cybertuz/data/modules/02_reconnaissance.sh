lab_koneksi_lokal() {
    clear
    echo -e "${YELLOW}=== LAB: ANALISIS KONEKSI LOKAL ===${RESET}"
    echo ""
    echo "Lab ini akan menganalisis koneksi jaringan di device kamu"
    echo ""
    echo -e "${WHITE}1. Cek IP Address:${RESET}"
    if command -v ip &>/dev/null; then
        ip addr show 2>/dev/null | grep -E "(inet |inet6)" | head -10
    elif command -v ifconfig &>/dev/null; then
        ifconfig 2>/dev/null | grep -E "inet " | head -10
    else
        echo "  ip/ifconfig tidak tersedia"
    fi
    echo ""
    echo -e "${WHITE}2. Cek Default Gateway dan Routing:${RESET}"
    ip route 2>/dev/null || route -n 2>/dev/null || echo "  Tidak tersedia"
    echo ""
    echo -e "${WHITE}3. Cek Koneksi Aktif:${RESET}"
    if command -v ss &>/dev/null; then
        ss -tuln 2>/dev/null | head -20
    elif command -v netstat &>/dev/null; then
        netstat -tuln 2>/dev/null | head -20
    else
        echo "  ss/netstat tidak tersedia"
    fi
    echo ""
    echo -e "${WHITE}4. Cek ARP Table:${RESET}"
    arp -a 2>/dev/null || ip neigh 2>/dev/null | head -10 || echo "  Tidak tersedia"
    echo ""
    echo -e "${WHITE}5. DNS Resolver:${RESET}"
    cat /etc/resolv.conf 2>/dev/null || echo "  /etc/resolv.conf tidak ada"
    pause_modul
}

