    echo "  /8  = 255.0.0.0      = 16.777.214 host"
    echo "  /16 = 255.255.0.0    = 65.534 host"
    echo "  /24 = 255.255.255.0  = 254 host"
    echo "  /25 = 255.255.255.128 = 126 host"
    echo "  /26 = 255.255.255.192 = 62 host"
    echo "  /27 = 255.255.255.224 = 30 host"
    echo "  /28 = 255.255.255.240 = 14 host"
    echo "  /29 = 255.255.255.248 = 6 host"
    echo "  /30 = 255.255.255.252 = 2 host"
    echo ""
    echo -e "${WHITE}PERHITUNGAN SUBNET:${RESET}"
    echo "  Contoh: 192.168.1.0/24"
    echo "  Network Address : 192.168.1.0"
    echo "  Broadcast       : 192.168.1.255"
    echo "  Usable Range    : 192.168.1.1 - 192.168.1.254"
    echo "  Total Host      : 254"
    echo ""
    echo -e "${WHITE}IPv6 (sekilas):${RESET}"
    echo "  128 bit, ditulis dalam 8 grup hexadecimal"
    echo "  Contoh: 2001:0db8:85a3:0000:0000:8a2e:0370:7334"
    echo "  Disingkat: 2001:db8:85a3::8a2e:370:7334"
    echo ""
    echo -e "${WHITE}SIMULASI SUBNET CALCULATOR:${RESET}"
    echo -ne "  Masukkan IP/CIDR (misal 192.168.1.0/24): "
    read -r ip_cidr
    if [[ "$ip_cidr" =~ ^([0-9]+\.[0-9]+\.[0-9]+\.[0-9]+)/([0-9]+)$ ]]; then
        local ip="${BASH_REMATCH[1]}"
        local cidr="${BASH_REMATCH[2]}"
        local hosts=$((2 ** (32 - cidr) - 2))
        echo ""
        echo "  IP       : $ip"
        echo "  CIDR     : /$cidr"
        echo "  Jumlah Host: $hosts"
    else
        echo "  $CT_INVALID - Format: X.X.X.X/XX"
    fi
    pause_modul
}

